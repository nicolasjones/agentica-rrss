# ─── TDD-005: is_manual flag ─────────────────────────

def test_ai_post_is_not_manual_by_default():
    """AI-generated posts should have is_manual=False (or None before DB flush)."""
    user = User(email="ai@test.com", hashed_password="x")
    band = Band(name="Sientes", owner=user, plan=PlanType.STARTER)
    batch = StrategicBatch(band=band, timeframe=BatchTimeframe.WEEKLY)
