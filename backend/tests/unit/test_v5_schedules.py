import pytest
from datetime import date, time

from app.models.models import (
    Band, User, PlanType,
    StrategicBatch, StrategicPost,
    BatchStatus, BatchTimeframe,
)


# ─── TDD-004: Schedules ───────────────────────────────

def test_strategic_post_accepts_scheduled_time():
    """StrategicPost can be created with a scheduled_time value."""
    user = User(email="sched@test.com", hashed_password="x")
    band = Band(name="Sientes", owner=user, plan=PlanType.STARTER)
    batch = StrategicBatch(band=band, timeframe=BatchTimeframe.WEEKLY)
    post = StrategicPost(
        batch=batch,
        platform="instagram",
        concept_title="Expectativa",
        narrative_goal="Generar hype",
        scheduled_date=date(2026, 5, 15),
        scheduled_time=time(20, 0),
    )
    assert post.scheduled_date == date(2026, 5, 15)
    assert post.scheduled_time == time(20, 0)


